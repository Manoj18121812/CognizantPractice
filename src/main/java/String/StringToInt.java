package String;

public class StringToInt {
    public static void main(String[] args) {

        //String to int
        String manoj = "123";
        int sum = Integer.parseInt(manoj);
        System.out.println(sum);
        //integer to string

        int num= 10;
        String rahul = Integer.toString(num);
        System.out.println(rahul);
    }
}
